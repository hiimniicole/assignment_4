function setup() {
	// create a place to draw
	createCanvas(640, 360);
	noStroke();
	noLoop();
}

function draw() {
	// clear the background
	background(81, 11, 11);

	// set a fill color
	fill(255, 255, 255);

	// draw the ellipse
	ellipse(56, 46, 55, 55);

	// set a fill color
	fill(9, 49, 214);

	// draw the blue ellipse
	ellipse(56, 46, 30, 30);

}
